;------------------------------------------------------------------
; Программа вывода сообщения на экран и ввода строки с клавиатуры
;------------------------------------------------------------------

;------------------- Объявление переменных ----------------
SECTION .data
msg: DB 'Введите строку:',10
msgLen: EQU $-msg
SECTION .bss
buf1: RESB 80

;------------------- Текст программы -----------------
SECTION .text
GLOBAL _start
_start:

;------------ Cистемный вызов `write`
mov eax,4
mov ebx,1
mov ecx,msg
mov edx,msgLen
int 80h

;------------ системный вызов `read` ----------------------
mov eax, 3
mov ebx, 0
mov ecx, buf1
mov edx, 80
int 80h

mov eax,4
mov ebx,1
mov ecx,buf1
mov edx,msgLen
int 80h

;------------ Системный вызов `exit` ----------------------
mov eax,1
mov ebx,0
int 80h

